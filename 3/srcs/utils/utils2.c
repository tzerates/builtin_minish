#include "../../inc/minishell.h"

int	ft_str_isdigit(char *str)
{
	int	i;

	i = 0;
	if(!str)
		return (0);
	if (str[i] == '-')
		i++;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (1);
		i++;
	}
	return (0);
}

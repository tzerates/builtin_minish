#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if (!s1 || !s2)
		return (NULL);
	while (s2[i] && n > i)
	{
		s1[i] = s2[i];
		i++;
	}
	while (n > i)
	{
		s1[i] = '\0';
		i++;
	}
	return (s1);
}
